package commands;

/**
 * Интерфейс, който дефинира команден шаблон за изпълнение на операции в приложението.
 * <p>
 * Всеки клас, който имплементира този интерфейс, трябва да предостави конкретна реализация
 * на метода {@link #execute(String[])}.
 */
public interface Command {

    /**
     * Изпълнява командата с подадените аргументи.
     *
     * @param args масив от аргументи, подадени към командата
     */
    void execute(String[] args);
}
